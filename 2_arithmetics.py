# 10 -Приоритет

"""
Задание:
Дано вычисление 70 * 3 + 4 / 8 + 2
Расставьте скобки так, чтобы оба сложения (3 + 4) и (8 + 2) высчитывались в первую очередь
Выведите на экран результат
"""

# Solution:

print((70 * (3 + 4)) / (8 + 2))

