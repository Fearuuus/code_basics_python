# 9 - Композиция операций

"""
Задание:
Реализуйте программу, которая вычисляет значение выражения 8 / 2 + 5 - -3 / 2. Не вычисляйте ничего самостоятельно,
ваша программа должна производить все вычисления сама
"""

# Solution:

print(((8 / 2) + 5) - (-3 / 2))

